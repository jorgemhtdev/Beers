import Foundation

// La carpeta "Preview Content" se crea automáticamente cuando se crea un proyecto de SwiftUI en Xcode, y se puede
// usar para agregar archivos de muestra que se utilizarán en vistas previas en la interfaz de usuario de Xcode.
// Al crear vistas previas de esta manera, los desarrolladores pueden ahorrar tiempo al evitar la necesidad de construir
// y ejecutar la aplicación para ver cómo se verá la vista en diferentes tamaños y dispositivos.

// Los datos de Preview Content no se incluyen en la aplicación final que se distribuye a los usuarios.
// Estos datos solo se utilizan para previsualizar el diseño de la interfaz de usuario durante el desarrollo.

// Cuando se ejecuta la aplicación en modo de producción, la carpeta "Preview Content" no se incluye en el
// paquete de la aplicación que se envía a la App Store o se instala en los dispositivos de los usuarios.

// Cuando se compila la aplicación para TestFlight o para distribución en la App Store,
// se genera un archivo de paquete (IPA) que contiene solo el código y los recursos necesarios para la aplicación.
// Esto significa que cualquier contenido o datos adicionales que se hayan utilizado para las vistas previas
// en Xcode no se incluirán en la aplicación final.
